package Shop.CustomerPackage;

public class ShoeDetails {

    int shoeID;
    String shoeName;
    String shoeSize;
    Float shoePrice;

    public ShoeDetails(int shoeID, String shoeName, String shoeSize, Float shoePrice){
        this.shoeID=shoeID;
        this.shoeName=shoeName;
        this.shoeSize=shoeSize;
        this.shoePrice=shoePrice;

    }

    public ShoeDetails() {}
}
