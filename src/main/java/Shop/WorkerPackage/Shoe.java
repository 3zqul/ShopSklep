package Shop.WorkerPackage;

public class Shoe {

    int shoeID;
    int shoeName;
    String shoeSize;
    Float shoeBuyPrice;
    Float shoeSellPrice;

    public Shoe(int shoeID, int shoeName, String shoeSize, Float shoeBuyPrice, Float shoeSellPrice){
        this.shoeID=shoeID;
        this.shoeName=shoeName;
        this.shoeSize=shoeSize;
        this.shoeBuyPrice=shoeBuyPrice;
        this.shoeSellPrice=shoeSellPrice;
    }
}
